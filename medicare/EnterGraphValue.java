package com.ruth.jahan.medicare;

public class EnterGraphValue {
    int time, weight;

    public EnterGraphValue() {
    }

    public EnterGraphValue(int time, int weight) {
        this.time = time;
        this.weight = weight;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }
}
